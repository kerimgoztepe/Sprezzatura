 package review_sessions.week4_variables_operators;

public class VariableDeclarations {

	public static void main(String[] args) {
		
        //Declare single variable
        int apples;

        //Declare multiple variables in single statement
        int bananas, oranges, mangoes, lemons;

        //declare single variable and assign value in 1 statement
        int kiwi = 2;

        //declare and assign value to multiple variables in single statement
        int watermelon = 4, grapes = 45, cherries = 33, apricots = 0;

	}

}
